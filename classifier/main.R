
num2Name <- function(class) {

    if (class == 1) return ("CLASS 1"); 
    if (class == 2) return ("CLASS 2");
    if (class == 3) return ("CLASS 3");
    if (class == 4) return ("CLASS 4");
    if (class == 5) return ("CLASS 5");

    print("NO CLASS FOUND. UPDATE LOOKUP.");
    return("NONE");

}

