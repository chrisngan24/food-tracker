
num2Name <- function(class) {

    if (class == 1) return ("APPLE"); 
    if (class == 2) return ("APPLE FUGI");
    if (class == 3) return ("APPLE RED");
    if (class == 4) return ("CARROT");
    if (class == 5) return ("CUCUMBER");

    print("NO CLASS FOUND. UPDATE LOOKUP.");
    return("NONE");

}

